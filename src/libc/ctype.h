#define isalnum(ch)  (_char_type_map[ch] & 0b00111000)
#define isalpha(ch)  (_char_type_map[ch] & 0b00110000)
#define islower(ch)  (_char_type_map[ch] & 0b00100000)
#define isupper(ch)  (_char_type_map[ch] & 0b00010000)
#define isdigit(ch)  (_char_type_map[ch] & 0b00001000)
#define isxdigit(ch) (_char_type_map[ch] & 0b00000100)
#define iscntrl(ch)  (_char_type_map[ch] & 0b01000000)
#define isgraph(ch)  (_char_type_map[ch] & 0b00111001)
#define isspace(ch)  (_char_type_map[ch] & 0b00000010)
#define isblank(ch)  (_char_type_map[ch] & 0b10000000)
#define isprint(ch)  (_char_type_map[ch] & 0b00111011)
#define ispunct(ch)  (_char_type_map[ch] & 0b00000001)
#define tolower(ch)  (isupper(ch) ? ch - 32 : ch)
#define toupper(ch)  (islower(ch) ? ch + 32 : ch)
